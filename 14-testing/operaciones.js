function sumar (a, b) {
    return a + b;
}

function restar (a, b) {
    return a - b;
}

function multiplicar (a, b) {
    return a * b;
}

function dividir (a, b) {
    return a / b;
}

function residuo (a, b) {
    return a % b;
}

module.exports = {
    sumar,
    restar,
    multiplicar,
    dividir,
    residuo
}
